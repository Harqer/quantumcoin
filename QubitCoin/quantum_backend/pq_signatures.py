class PQAlgorithm(str, Enum):
    """PQC implementation algorithms (internal layer)."""
    # NIST FIPS 204 — ML-DSA (formerly CRYSTALS-Dilithium)
    # liboqs 0.15.0+ uses standardized names
    ML_DSA_44 = "ML-DSA-44"         # NIST Level 2 (was Dilithium2)
    ML_DSA_65 = "ML-DSA-65"         # NIST Level 3 (was Dilithium3) — recommended
    ML_DSA_87 = "ML-DSA-87"         # NIST Level 5 (was Dilithium5)
    # NIST FIPS 205 — SPHINCS+
    SPHINCS_SHA2_128S = "SPHINCS+-SHA2-128s-simple"  # Hash-based fallback


# Backward-compatible aliases (user can still say "Dilithium3")
_ALGORITHM_ALIASES = {
    "Dilithium2": PQAlgorithm.ML_DSA_44,
    "Dilithium3": PQAlgorithm.ML_DSA_65,
    "Dilithium5": PQAlgorithm.ML_DSA_87,
    "ML-DSA-44": PQAlgorithm.ML_DSA_44,
    "ML-DSA-65": PQAlgorithm.ML_DSA_65,
    "ML-DSA-87": PQAlgorithm.ML_DSA_87,
    "SPHINCS+-SHA2-128s-simple": PQAlgorithm.SPHINCS_SHA2_128S,
}


def resolve_algorithm(name: str) -> PQAlgorithm:
    """Resolve algorithm name with backward compatibility."""
    if name in _ALGORITHM_ALIASES:
        return _ALGORITHM_ALIASES[name]
    try:
        return PQAlgorithm(name)
    except ValueError:
        valid = list(_ALGORITHM_ALIASES.keys())
        raise ValueError(f"Unknown algorithm: {name}. Valid: {valid}")


