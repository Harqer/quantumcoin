"""
Quantum Signatures — quantum-origin digital signatures on Base.

QuantumCoin Quantum Signatures are born from real quantum hardware:
  1. Key entropy is generated by QPU measurement (QNRG) — not classical PRNGs
  2. Entropy is certified by Bell inequality violation (DI-QKD, CHSH S > 2)
  3. Keys are quantum-safe against Shor's algorithm (NIST FIPS 204 Dilithium)

This is NOT just "post-quantum cryptography." PQC (Dilithium) is the
implementation layer — the quantum part is that every key, every signature,
originates from quantum measurement. No classical system can replicate this.

Quantum Signature = Quantum-Origin Entropy + PQC Algorithm + On-Chain Verification

Supported PQC algorithms (implementation layer):
  - ML-DSA-44  (Dilithium2)  — NIST Level 2, fastest, ~2.4 KB sigs
  - ML-DSA-65  (Dilithium3)  — NIST Level 3, recommended balance
  - ML-DSA-87  (Dilithium5)  — NIST Level 5, maximum security
  - SPHINCS+-SHA2-128s       — Hash-based fallback, most conservative

Architecture:
  QPU → QNRG entropy → Dilithium keygen → quantum-origin keypair (pk, sk)
  User → sk + message → Quantum Sign → signature with quantum provenance
  Chain → pk + message + signature → on-chain verify → ✅/❌
  ERC-4337 SmartWallet.validateUserOp() verifies quantum signatures on Base
"""

